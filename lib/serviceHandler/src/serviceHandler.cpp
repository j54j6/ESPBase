String ServiceHandler::formatComMessage(bool request, bool generateId, String serviceName, String MAC, String ip, String port, String id)
{
    /*
    {
        "type": "request",
        "serviceName" : "notSet",
        "mac" : "<<MACAddress>>",
        "ip" : "<<IPAddress>>",
        "servicePort" : "<<port>>"
        "id" : "<<id>>" //id is randomGenerated by millis()+RandomNumber
    }
    */

    #ifdef J54J6_SysLogger
        
        logging.logIt("formatMessage", "", 1);
        logging.logIt("formatMessage", "#####Debug#####", 1);
        logging.logIt("formatMessage", "Request: " + request, 1);
        logging.logIt("formatMessage", "GenerateID: " + generateId, 1);
        logging.logIt("formatMessage", "ServiceName: " + serviceName, 1);
        logging.logIt("formatMessage", "MAC: " + MAC, 1);
        logging.logIt("formatMessage", "IP: " + ip, 1);
        logging.logIt("formatMessage", "ServicePort: " + port, 1);
        logging.logIt("formatMessage", "ID: " + id, 1);
        logging.logIt("formatMessage", "#####Debug#####", 1);
        logging.logIt("formatMessage", "", 1);
    #endif 

    long genId = millis() + random(20, 2145895698);
    lastId = genId;

    String output = "{\"type\" : \"";
    if(request)
    {
        output += "request";
    }
    else
    {
        output += "answer";
    }
    output += "\",";
    output += "\"serviceName\" : \"";
    output += serviceName;
    output += "\", ";
    output += "\"mac\" : \"";
    output += MAC;
    output += "\", ";
    output += "\"ip\" : \"";
    output += ip;
    output += "\", ";
    output += "\"servicePort\" : \"";
    output += port;
    if(!generateId && id == "n.S")
    {
        output += "\"";
        output += "}";
    }
    else
    {
        if(id == "n.S")
        {
            output += "\",";
            output += "\"id\" : \"";
            output += genId;
            output += "\"}";
        }
        else
        {
            output += "\",";
            output += "\"id\" : \"";
            output += id;
            output += "\"}";
        } 
    }
    #ifdef J54J6_SysLogger
        logging.logIt("formatMessage", output.c_str(), -1);
    #endif 
    return output;
}

String ServiceHandler::getExternalServiceFilename(const char* serviceName, bool fallback)
{
    String cacheName = serviceName;
    cacheName.replace(" ", "");

    String rfilename = externalServicesPath;
    rfilename += cacheName;
    if(fallback)
    {
        rfilename += "-fallback";
    }
    rfilename += ".json";
    rfilename.replace(" ", "");
    return rfilename;
}

